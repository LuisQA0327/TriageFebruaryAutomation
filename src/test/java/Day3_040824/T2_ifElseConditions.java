public class T2_ifElseConditions {
    public static void main(String[] args) {

        //Just like in while loop, if else also uses something to be true or false to execute code

        int a = 10;
        int b = 20;

        if(a > b){
            System.out.println("Automation is awesome ");
        }else {
            System.out.println("Tomorrow is off woohoo! ");
        }//End of is else

    }//End of main
}//End of class

